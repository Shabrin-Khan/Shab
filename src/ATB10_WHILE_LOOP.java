public class ATB10_WHILE_LOOP {
    public static void main(String[] args) {

        //WHILE LOOP IS JUST CHANGING OF WRITING STYLE
        //FOR LOOP - A;B;C; eg -> for (int i = 0; i < 10; i++)

        //WHILE LOOP
        int i = 0;//A
        while(i<=10) { //B.condition in bracket
            System.out.println(i);
            i++;//C
        }




    }
}

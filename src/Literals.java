public class Literals {
    public static void main(String [] args) {
        int count = 827;
        System.out.println(count);
        int octal = 067;
        System.out.println(octal);
        int hexa = 0x67e;
        System.out.println(hexa);
        int binary = 0b11;
        System.out.println(binary);
        char ch1 = '\u0021';
        System.out.println(ch1);

        String str1 = null;
        System.out.println(str1);
    }
}
